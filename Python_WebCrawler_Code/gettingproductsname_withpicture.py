def getPageNum(l):
    r = requests.get(l,headers=header)
    objSoup = BeautifulSoup(r.text,'lxml')
    pageNum = objSoup.find_all('div','pagination')
    num = ''
    for i in pageNum:
        num = num + i.text
        num = num.split(' ')
        num = int(num[len(num)-2])
    return num

def setPageNum(lis,sLis):
    for i in lis:
        link = url + str(i) + '/products'
        sLis.append(getPageNum(link))

